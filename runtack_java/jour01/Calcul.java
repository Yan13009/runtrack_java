public class Calcul {
    public static void main(String[] args) {
        // Déclaration et initialisation de la variable nombre
        int nombre = 7;

        // Affichage de la valeur initiale de nombre
        System.out.println("Valeur initiale de nombre : " + nombre);

        // Ajout de 3 à la variable nombre
        nombre += 3;
        // Affichage de la nouvelle valeur de nombre
        System.out.println("Après avoir ajouté 3, nombre = " + nombre);

        // Multiplication de la valeur de nombre par 5
        nombre *= 5;
        // Affichage de la valeur finale de nombre
        System.out.println("Après avoir multiplié par 5, nombre = " + nombre);
    }
}
