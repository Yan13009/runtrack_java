public class DeclarationVariable {
    public static void main(String[] args) {
        // Déclaration de trois variables de type int et initialisation à 0
        int a = 0, b = 0, c = 0;

        // Affichage des valeurs des variables pour vérifier leur initialisation
        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
    }
}
