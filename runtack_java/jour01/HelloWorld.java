public class HelloWorld {
    // La méthode main est le point d'entrée du programme
    public static void main(String[] args) {
        // Affiche "Hello world" dans le terminal
        System.out.println("Hello world");

        /*
         * Cette méthode main t ne fait qu'une seule chose :
         * elle imprime une ligne de texte à l'écran. Ce type de programme est
         * souvent le premier exemple que les développeurs écrivent lorsqu'ils
         * apprennent un nouveau langage de programmation.
         */
    }
}
